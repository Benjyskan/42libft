#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*dest;
	int		i;

	dest = 0;
	if (!(dest = ((char*)malloc(sizeof(char) * ft_strlen(s1) + 1))))
		return (dest);
	i = 0;
	while (s1[i])
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = 0;
	return (dest);
}
