#include "libft.h"

int     ft_strncmp(const char *s1, const char *s2, size_t n)
{
	int		i;

	if (s1[0] == 0 && n > 0)
		return (-s2[0]);
	if (s2[0] == 0 && n > 0)
		return (s1[0]);
	i = 0;
	while (s1[i] && s2[i] && n > 0)
	{
		if (s1[i] != s2[i])
			return (s2[i] - s1[i]);
		i++;
		n--;
	}
	return (0);
}
